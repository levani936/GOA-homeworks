# ალგორითმი — ეს არის მკაფიო, დალაგებული ინსტრუქციების ნაკრები, რომელიც განსაზღვრავს როგორ უნდა მოგვარდეს კონკრეტული პრობლემა.
# Natural Language — ანუ ბუნებრივი ენა, როგორიცაა ქართული ან ინგლისური, რომელიც ადამიანებისთვის გასაგებია, მაგრამ კომპიუტერისთვის ზედმეტად ბუნდოვანია.
# Pseudocode — ეს არის ალგორითმის აღწერა ტექსტურად, რომელიც ძალიან ჰგავს კოდს, მაგრამ არ არის კონკრეტული პროგრამირების ენა.
# იგი გამოიყენება პროგრამის დაგეგმვისთვის, რადგან მარტივია წასაკითხად და გასაგებად.

# Sequence — ნიშნავს იმას, რომ მოქმედებები სრულდება ერთმანეთის მიყოლებით, ზუსტად იმ რიგით, როგორითაც ისინი დაწერილია.
x = 5      
y = 10     
z = x + y   
print(z)    

# Iteration (ციკლი, გამეორება) — ნიშნავს კოდის ნაწილს, რომელიც განმეორებით სრულდება მანამ, სანამ რაიმე პირობა არ შესრულდება.
# მაგალითად, დაბეჭდეთ რიცხვები 1-დან 5-მდე:
for i in range(1, 6):
    print(i)

# Selection — გულისხმობს პირობებზე დაფუძნებულ გადაწყვეტილების მიღებას.
# ანუ, პროგრამა ირჩევს მოქმედებას იმის მიხედვით, შესრულდა თუ არა პირობა.
# მაგალითი:
age = 18
if age >= 18:
    print("სრულწლოვანი ხარ")
else:
    print("არ ხარ სრულწლოვანი")

# დაბეჭდეთ თქვენი სახელი 20-ჯერ for ციკლის გამოყენებით:
